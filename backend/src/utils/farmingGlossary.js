/**
 * Apply the farming glossary to a translated Marathi string
 * Replaces formal words with colloquial farmer-friendly equivalents
 */
export function applyFarmingGlossary(text) {
  let result = text;
  for (const [formal, colloquial] of Object.entries(farmingGlossary)) {
    // Global replace (all occurrences)
    result = result.split(formal).join(colloquial);
  }
  return result;
}
